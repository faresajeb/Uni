public class WrapperClasses {
    public static void main(String[] args) {
        Boolean a = true;
        Character b = 'a';
        Integer c = 123;
        Double d = 123.45;
        String e = "hello";
        /*autoboxing: auto conversion that the Java compiler makes between
        the primitive types and their corresponding object wrapper class*/
        //access to more methods but slower than primitive data types

        if (a == true) {
            System.out.println("a == true");
        }
        if (b == 'a') {
            System.out.println("b == a");
        }
        //unboxing: reverse of autoboxing


    }
}
